#Mathematical Operations
# +,-,/,*,%,**,//

print ('3 + 1 is:', 3 + 1)
print ('3 - 1 is:', 3 - 1)
print ('6 / 2 is:', 6 / 2)
print ('7 / 2 is:', 7 / 2)
print ('7 // 2 is:', 7 // 2)
print ('7.3 // 2.478 is:', 7.3 // 2.478)
print ('7 % 2 is:', 7 % 2)
print ('6 % 2 is:', 6 % 2)
print ('7 * 2 is:', 7 * 2)
print ('7 ** 2 is:', 7 ** 2)
print ('4 ** 5 is:', 4 ** 5)

# What about other types? (String)
